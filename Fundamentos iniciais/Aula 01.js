// Sentença de Código e Bloco //

// As sentenças define o fluxo da sua aplicação //

// Os blocos agrupam essas sentenças para melhor organização da sua aplicação //

// Um bloco de código é um agrupamento de sentenças de código //

// O comando "console.log()" é utilizado para exibir algo na tela do computador //

console.log(1);

console.log(1 + 3);

console.log('Texto');

// com a extensão posso escrever somente "clg" q ele ja faz o "console.log" //

